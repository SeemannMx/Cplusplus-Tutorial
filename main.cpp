#include <iostream>

// using standard library
using namespace std;

/**
 * start main in C++ to start tutorial
 *
 * @return
 */
int main() {
    // print out to same line
    cout << "Hello ";
    cout << "World \n";

    // blank line
    cout << "\n";

    // print out to new line
    cout << "Hello" << endl;
    cout << " \t  World";
    return 0;
}